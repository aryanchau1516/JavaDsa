import java.util.Stack;

public class stack{
    public static void main(String[] args) {
        Stack<Integer> stack = new Stack<>();
        stack.push(3);
        stack.push(7);
        stack.push(8);
        stack.push(5);
        stack.push(3);
        
        System.out.println(stack);
        stack.pop();
        System.out.println(stack);
        
        


                        
}
}


